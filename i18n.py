def get_text(lang: str, key: str, **kwargs) -> str:
    """Get translated text, falling back to EN."""
    texts = TEXTS.get(lang, TEXTS["en"])
    text = texts.get(key, TEXTS["en"].get(key, key))
    return text.format(**kwargs) if kwargs else text


